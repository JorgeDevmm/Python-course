# Set significa grupo o conjunto(no se puede repetir y no esta ordenada)

primer = {1,1,2,2,3,4}
segundo = [3,4,5]

# agregar un elemento a los set
# primer.add(5)

# eliminar un elemento a los set
# primer.remove(1)

# convertir una lista a un set
segundo = set(segundo)

print(primer)
print(segundo)

# unir dos sets y elimina los duplicados
print(primer | segundo)

# aplicando intersección, elementos que estan en ambos sets
print(primer & segundo)


# elimina elemento del primer set que coincidan en el segunto set
print(primer - segundo)

# mostrar la diferencia simetrica, quita de ambos set lo que coincida
print(primer ^ segundo)


# no podemos acceder x posiciónes a un set, ya que no estan ordenados, pero podemos consultar

if 5 in segundo:
    print("Hola mundo")
